// Helper function to determine the stock status classes
export const getStockStatus = (quantity: number) => {
  if (quantity <= 0) {
    return {
      text: "Out of Stock",
      bgClass: "bg-red-500",
    };
  }

  const percentage = (quantity / 100) * 100;

  if (quantity > 0 && percentage <= 33.3) {
    return {
      text: "Few Stock",
      bgClass: "bg-yellow-500",
    };
  } else {
    return {
      text: "In Stock",
      bgClass: "bg-green-500",
    };
  }
};
